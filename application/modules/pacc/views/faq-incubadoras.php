                            <a data-toggle="collapse" data-parent="#accordion-cat-1" href="#faq-cat-1-sub-2">
                                <h4 class="panel-title title-faq">
                                  ¿Cómo generar la asociación de datos a usuarios DNA2- DIRCON - UCAP?
                                    <span class="pull-right"><i class="glyphicon glyphicon-plus"></i></span>
                                </h4>
                            </a>
                        </div>
                        <div id="faq-cat-1-sub-2" class="panel-collapse collapse">
                            <div class="panel-body respuesta-faq">
                                Consultores inscriptos en el Directorio de Consultores con Nro. de DIRCON inferior a 20.000<br /><br />
                                Para poder asociar su inscripción al DIRCON con su Usuario DNA2 tiene que ingresar a <a href="http://www.accionpyme.mecon.gov.ar/dna2">http://www.accionpyme.mecon.gov.ar/dna2</a> y generar un nuevo usuario (si ya estuviera registrado no es necesario realizar una nueva registración). Una vez obtenido el usuario debe enviar un correo electrónico a <a href="mailto:dna2@industria.gob.ar">dna2@industria.gob.ar</a> con los siguientes datos:<br /><br />
                                Asunto del mail: Asociar DIRCON<br />
                                Texto del mail (no omitir ninguno de los siguientes datos):<br />
                                Nombre de Usuario DNA2<br />
                                DNI del Usuario DNA2<br />
                                Dirección de mail del Usuario DNA2<br />
                                Nombre o Razón Social de la inscripión en el DIRCON<br />
                                CUIT de la inscripión en el DIRCON<br />
                                Nro de DIRCON asignado<br /><br />

                                A la brevedad se le responderá el mail y Ud. podrá verificar / actualizar los datos ingresando con su nombre de Usuario DNA2 a la siguiente dirección <a href="http://www.accionpyme.mecon.gov.ar/dna2">http://www.accionpyme.mecon.gov.ar/dna2</a> “Alta de Unidades Capacitadoras y Consultoras (UCAP y DIRCON) <a href="http://www.accionpyme.mecon.gov.ar/dna2/appfront/aplicacion2.php?idap=234">www.accionpyme.mecon.gov.ar/dna2/appfront/aplicacion2.php?idap=234</a> / Listado de Inscripciones Cargadas <a href="http://www.accionpyme.mecon.gov.ar/dna2/appfront/listar.php?idvista=1338&idap=234&gt;">http://www.accionpyme.mecon.gov.ar/dna2/appfront/listar.php?idvista=1338&idap=234&gt;</a><br /><br />
                                Usuarios con UCAP ingresada en DNA2 e inscripción al DIRCON registrada con número inferior a 20.000<br /><br />
                                Su Usuario DNA2 ya debe tener los datos correspondientes al Directorio de Consultores asociados a su UCAP, los puede visualizar ingresando a <a href="http://www.accionpyme.mecon.gov.ar/dna2">http://www.accionpyme.mecon.gov.ar/dna2</a> “Alta de Unidades Capacitadoras y Consultoras (UCAP y DIRCON) <a href="http://www.accionpyme.mecon.gov.ar/dna2/appfront/aplicacion2.php?idap=234">www.accionpyme.mecon.gov.ar/dna2/appfront/aplicacion2.php?idap=234</a> / Listado de Inscripciones Cargadas <a href="http://www.accionpyme.mecon.gov.ar/dna2/appfront/listar.php?idvista=1338&idap=234&gt;">http://www.accionpyme.mecon.gov.ar/dna2/appfront/listar.php?idvista=1338&idap=234&gt;</a><br /><br />

                                Si Ud. no tuviera el número de DIRCON asociado a su UCAP deberá enviarnos un mail a <a href="mailto:dna2@industria.gob.ar">dna2@industria.gob.ar</a> con los siguientes datos:<br /><br />
                                Asunto del mail: Asociar DIRCON<br />
                                Texto del mail (no omitir ninguno de los siguientes datos):<br />
                                Nombre de Usuario DNA2<br />
                                DNI del Usuario DNA2<br />
                                Dirección de mail del Usuario DNA2<br />
                                Nombre o Razón Social de la inscripión en el DIRCON<br />
                                CUIT de la inscripión en el DIRCON<br />
                                Nro de DIRCON asignado<br /><br />
                                A la brevedad se le responderá el mail y Ud. podrá verificar / actualizar los datos de acuerdo a los nuevos requerimientos ingresando son su nombre de Usuario DNA2.<br /><br />
                                Usuarios con UCAP ingresada en DNA2 y que quieren inscribirse en el DIRCON<br /><br />
                                Ud. deberá modificar los datos registrados y en el campo: <br />
                                Seleccionar la Opción “Ambos” y completar los datos que le sean requeridos hasta la ventana “Reporte Inscripción”, alli deberá hacer click en el link “Para confirmar todos los datos y Registrar”, una vez realizada está acción se le informará el número de inscripción obtenido.
