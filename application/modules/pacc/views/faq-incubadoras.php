                                    ¿Qué productos verificables debería presentar?
                                    <span class="pull-right"><i class="glyphicon glyphicon-plus"></i></span>
                                </h4>
                            </a>
                        </div>
                        <div id="faq-cat-1-sub-3" class="panel-collapse collapse">
                            <div class="panel-body">
                               
                                Los productos verificables deben dar constancia de la efectiva realización de las tareas descriptas en el proyecto, demostrando el contenido y alcance previamente definidos y los procedimientos realizados, a los efectos de sustentar el presupuesto asociado.
                                No es el PACC quien debe establecer cuales son los productos verificables que se deberían presentar, porque nadie mejor que el que va a realizar la tarea para saber qué elementos va a poder presentar para demostrar la realización de las mismas.
                                Cuando se presente el proyecto, la evaluación técnica determinará si los PV propuestos son suficientes para cumplir el objetivo descripto y si no es así, hará las observaciones correspondientes.
                                
